                        <h4>Familiarity </h4>
                        <p>Have you noticed that the icon for saving files is the same purple floppy disk on nearly all applications, whether they are on the PC, Mac or a smartphone? That is because people are familiar with it. When they see that icon, they instantly recognize that it is for saving files. Zero cognitive overhead. </p>
                        <br/>
                        <p>Make sure your design is familiar to your users. If your web page has a lot of functions, create a tabbed interface or something that looks familiar. The easier it is to use, the more people will use it. A familiar UI is of utmost importance for customer engagement and conversion rates. </p>
                        
                        <h4>Responsive </h4>
                        <p>This means, your interface has to be quick to respond to user actions. It should also indicate what is happening. Something like the Gmail loading page tells users how far the page has loaded and does not leave them hanging, waiting for something to happen. You can apply this to any number of user interface elements. </p>

