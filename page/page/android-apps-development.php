                        <p>The SDK is available as a standalone application on Windows, OS X and Linux. However it also comes as part of Android Studio. Depending on how much functionality you want, you can get Studio or only download the SDK. Keep your SDK updated because as Android gains more features every year, this is updated to provide more libraries and API’s to use those features in native app. </p>
                        
                        <h4>The NDK  </h4>
                        <p>The Android NDK is a specialized form of the software development kit. You want to use this instead of, or in addition, to the SDK when you want to make an app that required more CPU resources. The NDK will:</p>
                        <ul>
                            <li>Provide closer access to hardware for processor intensive tasks</li>
                            <li>Let you edit native code so that you can optimize your app even more closely</li>
                        </ul>
                        
                        <p>However, remember that you don’t need the NDK for simple apps. Using the NDK also means that you need to have more knowledge about the inner workings of Android. You are responsible for handling low level details of the Dalvik or ART virtual machines that run Android on smartphones. </p>
                        
                        <h4>Android Studio  </h4>
                        <p>Most of the development on Android was done on Eclipse before 2014. This is because native development on this platform is mostly done in Java. However, the newer version of Android Studio (2.0) uses IntelliJ IDEA instead. This is also free and open source. </p>
                        <br/>
                        <p>You can still use Eclipse if you are comfortable with that IDE for native Android apps, as millions of developers do. However it is no longer offered officially and directly by Google. Developing apps on Android is much easier today than it was even 2 or 3 years ago because of improvements in Android itself and the development tools surrounding it. <br/><br/><br/><br/></p>

