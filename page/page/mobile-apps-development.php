                        <h4>No Perfect Product</h4>
                        <p>Many professional have the idea of a perfect app that will do everything right the first time. The reality is that:</p>
                        <ul>
                            <li>You don’t know exactly how users will use your app until it is in their hands</li>
                            <li>You can’t predict what features they will need in future</li>
                            <li>You won’t know what is wrong with your app unless it is extensively used by customers</li>


                        </ul>
                        
                        <p>Therefore, focus on experimenting with your features and design shipping them. This does not mean you can ship an unstable app. Decide on a feature and complete it and let users tell you (with usage data) about what more or less they want.</p>
                        
                        <h4>Simple vs. Simplistic</h4>
                        <p>There is an increasing trend in app development towards design minimalism. This is a good idea, but some tend to take it too far. Whatever feature your app has, make it visible and easily accessible. There is a difference between an app that work in a simple manner and one that has the bare minimum. Workflow is more important than hiding UI elements. </p>
                        <br/>
                        <p>It is also very important for you to consider what difference it makes for customers between a web app and a native app. If your app functions are simple enough, a web app may be more appropriate for your users. <br/><br/><br/><br/></p>

                        
