                        <p>When you have data fields for users to enter information, make sure you have set the right categories and the right accounts. This first step will set the foundation for reliable accounting for the lifetime of the account. </p>
                        
                        <h4>Setting up accounts</h4>
                        <p>If the user does not have a Xero account, and you want them to be able to do that right from your website or application, here is what is best:</p>
                        <ul>
                            <li>Make sure you provide sane defaults like an account that already exists in a particular category</li>
                            <li>Offer to create new accounts (other than the defaults provided by Xero), since they may have their own custom accounting setup</li>
                            <li>Don’t automatically create a new account without asking the user</li>


                        </ul>
                        

                        <h4>Validate your mapping  </h4>
                        <p>Before you export accounts, always make sure that you have validated the users’ accounts. They may change the account structure even after they have created new ones. If you miss these changes when exporting, customers may lose valuable data. </p>

                        <h4>Set taxation for different regions </h4>
                        <p>Taxation is different in every country, so you need to design your tax calculation depending on which country you offer the service in. For the UK and Canada for example, you can use some default tax rates. This is not true for the US and some other countries though. </p>

                        <h4>Invoicing </h4>
                        <p>You can link a Xero invoice to your application from the user account. When you do this on your domain, make sure that the URL does not contain ‘xero’, as it will throw a validation error. Use your own application name for this and you will have error-free invoicing. <br/><br/><br/><br/></p>


