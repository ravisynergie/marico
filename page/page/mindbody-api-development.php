                        <h4>How many calls can you make to the API?</h4>
                        <p>Currently, MINDBODY allows about 1000 calls to their API for free for any web application. Any more calls after those are charged about $0.33. As a small website this is probably enough for testing your application with live data. You can visit their developer site or contact them via email for more custom pricing for API calls.</p>

                        <h4>Which credit cards are allowed to be processed by MINDBODY?</h4>
                        <p>While you can process credit cards using the API, the support for merchants is quite limited at this point. For instance,</p>
                        <ul>
                            <li>Your StudioID for MINDBODY must have an active merchant account with the service</li>
                            <li>At this time, only two processors viz. Transfirst and Bluefin are available with the service</li>

                        </ul>
                        <br/>
                        <p>You can use their credit card processing requests like <kbd><</kbd>CheckoutShoppingCart<kbd>></kbd> and <kbd><</kbd>FinderCheckoutShoppingCart<kbd>></kbd>. However, this requires that you to be a Finder partner. You can find out more about their Finder Service program on their website.</p>
                        
                        <h4>What if more features are desired from the API?</h4>
                        <p>The MINDBODY API is already quite feature rich and it is still growing. If you have noticed a certain feature missing or want to add a feature, you can send in suggestions at their API forums. There is a healthy community there, monitored by the company and they respond very frequently with information about how they are processing new requests.</p>

                        <h4>Where can users find code examples to learn more about the API? </h4>
                        <p>You can find code examples about how to use the MINDBODY API on their Github page. The current examples are based mainly on Python, Ruby, C# and PHP, but more languages are planned in their future. However, it is quite easy to deduce what kind of code to write in your preferred language based on the examples already present there. </p>

                        <h4>Which version of the API should be used for the best results?</h4>
                        <p>API version 0.5 is the more recent and actively supported right now. While v0.4 can still be used, it is basically on life support with only security patches and updates for fatal errors. <br/><br/><br/><br/></p>


